// Variable - a container in which we can store values for later retrieval

/** Rules when creating variables in JS
 * 1. Use camelCase naming convention
 * 2. Case senstitive (all variables should start with a small letter)
 * 3. We should not use keywords (reserved words for a programming language)
 * 4. Should not start with a number
 */

